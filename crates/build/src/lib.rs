//! civ-build — Procedural voxel building grammar + freehand authoring; produces shared BuildingGraph
//!
//! Part of the Civis 3D extension (feat/civis-3d-foundation).
//! See `docs/roadmap/civis-3d-extension.md` for the full design context.
//!
//! Functional requirements: FR-CIV-BUILD-*

#![forbid(unsafe_code)]
#![warn(missing_docs)]

use std::collections::BTreeMap;

use civ_voxel::{MaterialId, WorldCoord};
use rand::{Rng, SeedableRng};
use rand_chacha::ChaCha8Rng;
use serde::{Deserialize, Serialize};

/// Provenance tag carried by every building diff so the renderer can style
/// procedural-grown vs user-freehand structures differently if desired.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum BuildingProvenance {
    /// Generated by the autonomous demand-driven allocator.
    Procedural,
    /// Authored by the user in freehand mode.
    Freehand,
}

/// Marker version of this crate's public schema. Bumped on breaking changes
/// so replay (`.civreplay`) files can refuse to load mismatched versions.
pub const SCHEMA_VERSION: u32 = 0;

/// Stable identifier for a building parcel.
#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash, Serialize, Deserialize)]
pub struct BuildingId(pub u64);

/// Parcel classification used by the city growth grammar.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum ParcelKind {
    /// Housing and associated neighborhood services.
    Residential,
    /// Shops, offices, and trade-facing frontage.
    Commercial,
    /// Production, storage, and extractive use.
    Industrial,
    /// Public and administrative buildings.
    Civic,
}

/// A buildable land parcel and its placement constraints.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct Parcel {
    /// Stable parcel identity.
    pub id: BuildingId,
    /// Land-use classification.
    pub kind: ParcelKind,
    /// World-space origin of the parcel.
    pub origin: WorldCoord,
    /// Parcel extent in fixed-scale world units.
    pub size: [u16; 3],
    /// Minimum era required before this parcel may be instantiated.
    pub era_min: u16,
}

/// Era-driven facade grammar for a building.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct FacadeStyle {
    /// Style label used by the grammar and downstream tooling.
    pub name: String,
    /// Earliest era this style belongs to.
    pub era: u16,
    /// Ordered material palette for the facade.
    pub materials: Vec<MaterialId>,
    /// Roof pitch in degrees.
    pub roof_pitch_deg: u16,
    /// Window density from sparse to dense.
    pub window_density: u8,
}

/// Input signals that drive procedural parcel allocation.
#[derive(Debug, Clone, Copy, PartialEq, Serialize, Deserialize)]
pub struct DemandSignals {
    /// Residential demand in the inclusive range [0.0, 1.0].
    pub residential: f32,
    /// Commercial demand in the inclusive range [0.0, 1.0].
    pub commercial: f32,
    /// Industrial demand in the inclusive range [0.0, 1.0].
    pub industrial: f32,
    /// Civic demand in the inclusive range [0.0, 1.0].
    pub civic: f32,
}

/// Deterministic allocator that grows parcels from demand signals.
#[derive(Debug, Clone)]
pub struct Allocator {
    rng: ChaCha8Rng,
    next_id: u64,
}

impl Allocator {
    /// Creates a deterministic allocator from a seed.
    #[must_use]
    pub fn new(seed: u64) -> Self {
        Self {
            rng: ChaCha8Rng::seed_from_u64(seed),
            next_id: 1,
        }
    }

    /// Allocates one parcel per saturated demand signal.
    pub fn allocate(
        &mut self,
        graph: &mut BuildingGraph,
        signals: &DemandSignals,
        era: u16,
        origin: WorldCoord,
        region_extent_voxels: u32,
    ) -> Vec<BuildingId> {
        let mut allocated = Vec::new();
        let extent = region_extent_voxels.max(1);
        let kinds = [
            (ParcelKind::Residential, signals.residential),
            (ParcelKind::Commercial, signals.commercial),
            (ParcelKind::Industrial, signals.industrial),
            (ParcelKind::Civic, signals.civic),
        ];

        for (kind, signal) in kinds {
            if signal <= 0.5 {
                continue;
            }

            let id = BuildingId(self.next_id);
            self.next_id += 1;

            let parcel = Parcel {
                id,
                kind,
                origin: WorldCoord {
                    x: origin
                        .x
                        .saturating_add(i64::from(self.rng.gen_range(0..extent))),
                    y: origin
                        .y
                        .saturating_add(i64::from(self.rng.gen_range(0..extent))),
                    z: origin
                        .z
                        .saturating_add(i64::from(self.rng.gen_range(0..extent))),
                },
                size: [extent.min(u16::MAX as u32) as u16; 3],
                era_min: era,
            };

            graph.insert_parcel(parcel);
            graph.set_provenance(id, BuildingProvenance::Procedural);
            graph.set_facade(id, default_facade_for_era(era));
            allocated.push(id);
        }

        allocated
    }
}

/// Shared building graph for both autonomous growth and freehand authoring.
#[derive(Debug, Clone, PartialEq, Eq, Default, Serialize, Deserialize)]
pub struct BuildingGraph {
    /// All known parcels in deterministic order.
    pub parcels: Vec<Parcel>,
    /// Facade style per parcel.
    pub facades: BTreeMap<BuildingId, FacadeStyle>,
    /// Provenance tag per parcel.
    pub provenance: BTreeMap<BuildingId, BuildingProvenance>,
}

impl BuildingGraph {
    /// Creates an empty building graph.
    #[must_use]
    pub fn new() -> Self {
        Self::default()
    }

    /// Inserts or replaces a parcel.
    pub fn insert_parcel(&mut self, parcel: Parcel) {
        if let Some(existing) = self.parcels.iter_mut().find(|p| p.id == parcel.id) {
            *existing = parcel;
        } else {
            self.parcels.push(parcel);
        }
    }

    /// Assigns a facade style to a parcel.
    pub fn set_facade(&mut self, id: BuildingId, style: FacadeStyle) {
        self.facades.insert(id, style);
    }

    /// Assigns a provenance tag to a parcel.
    pub fn set_provenance(&mut self, id: BuildingId, provenance: BuildingProvenance) {
        self.provenance.insert(id, provenance);
    }

    /// Returns parcels whose `era_min` is at or below the supplied era.
    pub fn parcels_at_era(&self, era: u16) -> impl Iterator<Item = &Parcel> {
        self.parcels
            .iter()
            .filter(move |parcel| parcel.era_min <= era)
    }

    #[cfg(test)]
    /// Serializes and deserializes the graph through RON for round-trip tests.
    fn round_trip_ron(&self) -> Self {
        let encoded = ron::to_string(self).expect("serialize BuildingGraph to RON");
        ron::from_str(&encoded).expect("deserialize BuildingGraph from RON")
    }
}

/// Returns the default facade for a given era.
#[must_use]
pub fn default_facade_for_era(era: u16) -> FacadeStyle {
    match era {
        0 => FacadeStyle {
            name: "mud-brick".to_owned(),
            era,
            materials: vec![MaterialId(1)],
            roof_pitch_deg: 0,
            window_density: 0,
        },
        1 => FacadeStyle {
            name: "timber".to_owned(),
            era,
            materials: vec![MaterialId(2)],
            roof_pitch_deg: 30,
            window_density: 1,
        },
        2 => FacadeStyle {
            name: "stone".to_owned(),
            era,
            materials: vec![MaterialId(3)],
            roof_pitch_deg: 35,
            window_density: 2,
        },
        3 => FacadeStyle {
            name: "brick".to_owned(),
            era,
            materials: vec![MaterialId(4)],
            roof_pitch_deg: 30,
            window_density: 3,
        },
        4 => FacadeStyle {
            name: "reinforced_concrete".to_owned(),
            era,
            materials: vec![MaterialId(5)],
            roof_pitch_deg: 5,
            window_density: 5,
        },
        5 => FacadeStyle {
            name: "arcology".to_owned(),
            era,
            materials: vec![MaterialId(6)],
            roof_pitch_deg: 0,
            window_density: 8,
        },
        _ => FacadeStyle {
            name: format!("era-{era}"),
            era,
            materials: vec![MaterialId(6)],
            roof_pitch_deg: 0,
            window_density: 8,
        },
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    /// FR-CIV-BUILD-000 — crate compiles and exposes a schema version.
    #[test]
    fn schema_version_present() {
        assert_eq!(SCHEMA_VERSION, 0);
    }

    /// FR-CIV-BUILD-001 — BuildingGraph round-trips through RON.
    #[test]
    fn build_001_building_graph_round_trips_through_ron() {
        let mut graph = BuildingGraph::new();
        let id = BuildingId(42);
        graph.insert_parcel(Parcel {
            id,
            kind: ParcelKind::Civic,
            origin: WorldCoord { x: 1, y: 2, z: 3 },
            size: [12, 8, 4],
            era_min: 2,
        });
        graph.set_facade(id, default_facade_for_era(2));
        graph.set_provenance(id, BuildingProvenance::Procedural);

        assert_eq!(graph, graph.round_trip_ron());
    }

    /// FR-CIV-BUILD-002 — default_facade_for_era covers eras 0..=5 with non-empty materials.
    #[test]
    fn build_002_default_facade_for_era_covers_0_to_5() {
        for era in 0..=5 {
            let facade = default_facade_for_era(era);
            assert_eq!(facade.era, era);
            assert!(!facade.materials.is_empty());
        }
    }

    /// FR-CIV-BUILD-003 — parcels_at_era filters correctly.
    #[test]
    fn build_003_parcels_at_era_filters_correctly() {
        let mut graph = BuildingGraph::new();
        graph.insert_parcel(Parcel {
            id: BuildingId(1),
            kind: ParcelKind::Residential,
            origin: WorldCoord { x: 0, y: 0, z: 0 },
            size: [1, 1, 1],
            era_min: 0,
        });
        graph.insert_parcel(Parcel {
            id: BuildingId(2),
            kind: ParcelKind::Commercial,
            origin: WorldCoord { x: 1, y: 0, z: 0 },
            size: [1, 1, 1],
            era_min: 2,
        });

        let parcels: Vec<_> = graph.parcels_at_era(1).collect();
        assert_eq!(parcels.len(), 1);
        assert_eq!(parcels[0].id, BuildingId(1));
    }

    /// FR-CIV-BUILD-004 — inserting + retrieving a parcel preserves all fields.
    #[test]
    fn build_004_insert_and_retrieve_parcel_preserves_fields() {
        let mut graph = BuildingGraph::new();
        let parcel = Parcel {
            id: BuildingId(99),
            kind: ParcelKind::Industrial,
            origin: WorldCoord { x: 7, y: 11, z: 13 },
            size: [17, 19, 23],
            era_min: 4,
        };
        graph.insert_parcel(parcel.clone());

        assert_eq!(graph.parcels, vec![parcel]);
    }

    /// FR-CIV-BUILD-005 — provenance tagging (procedural vs freehand) round-trips.
    #[test]
    fn build_005_provenance_tagging_round_trips() {
        let mut graph = BuildingGraph::new();
        let id = BuildingId(7);
        graph.insert_parcel(Parcel {
            id,
            kind: ParcelKind::Residential,
            origin: WorldCoord { x: 3, y: 4, z: 5 },
            size: [6, 7, 8],
            era_min: 1,
        });
        graph.set_provenance(id, BuildingProvenance::Freehand);

        let back = graph.round_trip_ron();
        assert_eq!(
            back.provenance.get(&id),
            Some(&BuildingProvenance::Freehand)
        );
    }

    /// FR-CIV-BUILD-010 — zero signals produce no parcels.
    #[test]
    fn build_010_zero_signals_produce_no_parcels() {
        let mut graph = BuildingGraph::new();
        let mut allocator = Allocator::new(7);
        let signals = DemandSignals {
            residential: 0.0,
            commercial: 0.0,
            industrial: 0.0,
            civic: 0.0,
        };

        let ids = allocator.allocate(&mut graph, &signals, 1, WorldCoord { x: 0, y: 0, z: 0 }, 16);

        assert!(ids.is_empty());
        assert!(graph.parcels.is_empty());
    }

    /// FR-CIV-BUILD-011 — high residential signal places at least one Residential parcel.
    #[test]
    fn build_011_high_residential_signal_places_residential_parcel() {
        let mut graph = BuildingGraph::new();
        let mut allocator = Allocator::new(7);
        let signals = DemandSignals {
            residential: 1.0,
            commercial: 0.0,
            industrial: 0.0,
            civic: 0.0,
        };

        let ids = allocator.allocate(
            &mut graph,
            &signals,
            2,
            WorldCoord {
                x: 10,
                y: 20,
                z: 30,
            },
            32,
        );

        assert!(!ids.is_empty());
        assert!(graph
            .parcels
            .iter()
            .any(|parcel| parcel.kind == ParcelKind::Residential));
    }

    /// FR-CIV-BUILD-012 — deterministic placement with same seed and input.
    #[test]
    fn build_012_deterministic_placement_is_reproducible() {
        let signals = DemandSignals {
            residential: 1.0,
            commercial: 1.0,
            industrial: 0.0,
            civic: 0.0,
        };
        let origin = WorldCoord { x: 4, y: 5, z: 6 };

        let mut graph_a = BuildingGraph::new();
        let mut graph_b = BuildingGraph::new();
        let mut allocator_a = Allocator::new(99);
        let mut allocator_b = Allocator::new(99);

        let ids_a = allocator_a.allocate(&mut graph_a, &signals, 3, origin, 24);
        let ids_b = allocator_b.allocate(&mut graph_b, &signals, 3, origin, 24);

        assert_eq!(ids_a, ids_b);
        assert_eq!(graph_a.parcels, graph_b.parcels);
    }

    /// FR-CIV-BUILD-013 — mixed signals produce one parcel per saturated kind.
    #[test]
    fn build_013_mixed_signals_produce_one_parcel_per_saturated_kind() {
        let mut graph = BuildingGraph::new();
        let mut allocator = Allocator::new(123);
        let signals = DemandSignals {
            residential: 0.9,
            commercial: 0.75,
            industrial: 0.0,
            civic: 0.6,
        };

        let ids = allocator.allocate(&mut graph, &signals, 4, WorldCoord { x: 0, y: 0, z: 0 }, 8);

        assert_eq!(ids.len(), 3);
        assert_eq!(graph.parcels.len(), 3);
        assert!(graph
            .parcels
            .iter()
            .any(|parcel| parcel.kind == ParcelKind::Residential));
        assert!(graph
            .parcels
            .iter()
            .any(|parcel| parcel.kind == ParcelKind::Commercial));
        assert!(graph
            .parcels
            .iter()
            .any(|parcel| parcel.kind == ParcelKind::Civic));
    }

    /// FR-CIV-BUILD-014 — facade era matches the era argument.
    #[test]
    fn build_014_facade_era_matches_era_argument() {
        let mut graph = BuildingGraph::new();
        let mut allocator = Allocator::new(5);
        let signals = DemandSignals {
            residential: 1.0,
            commercial: 0.0,
            industrial: 0.0,
            civic: 0.0,
        };

        let ids = allocator.allocate(&mut graph, &signals, 5, WorldCoord { x: 0, y: 0, z: 0 }, 12);

        assert_eq!(ids.len(), 1);
        let id = ids[0];
        assert_eq!(graph.facades.get(&id).map(|facade| facade.era), Some(5));
        assert_eq!(graph.facades.get(&id), Some(&default_facade_for_era(5)));
    }

    /// FR-CIV-BUILD-015 — 100 ticks of high signals produce around 100 parcels.
    #[test]
    fn build_015_hundred_ticks_of_high_signals_produce_around_hundred_parcels() {
        let mut graph = BuildingGraph::new();
        let mut allocator = Allocator::new(11);
        let signals = DemandSignals {
            residential: 0.9,
            commercial: 0.0,
            industrial: 0.0,
            civic: 0.0,
        };

        for _ in 0..100 {
            allocator.allocate(&mut graph, &signals, 1, WorldCoord { x: 0, y: 0, z: 0 }, 4);
        }

        assert!((95..=105).contains(&graph.parcels.len()));
    }
}

/// One delta event for a single chunk. The renderer pairs `event` with the
/// `voxels` payload to re-mesh the affected chunk.
///
/// Wire layout: `event` is ~24 bytes (ChunkId u64 + WriteSeq u64 + bounds in a
/// follow-up). `voxels` is the dense leaf payload (`16^3 = 4096` voxels) packed
/// as `MaterialId` u16s = 8 KiB per dirty chunk before zstd. Practical
/// production framing will be either:
///
/// - Delta-encoded RLE over `voxels` for cheap chunks
/// - Whole-chunk replacement for fully-edited chunks
///
/// Both encodings ride on top of this type; the encoding tag lands in the
/// follow-up PR that introduces the wire format.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct VoxelChunkDelta {
    /// The drained dirty event that triggered this delta.
    pub event: DirtyChunkEvent,
    /// Full dense-leaf voxel payload for the affected chunk.
    pub voxels: Vec<MaterialId>,
}

/// A batch of voxel deltas delivered after one server tick. The batch is always
/// pre-sorted by `(chunk_id, write_seq)` (the kernel guarantees this via
/// `VoxelWorld::drain_dirty`); clients can rely on iteration order being
/// identical across machines.
#[derive(Debug, Clone, PartialEq, Eq, Default, Serialize, Deserialize)]
pub struct VoxelDeltaFrame {
    /// Server tick at which the batch was produced.
    pub tick: u64,
    /// One entry per affected chunk.
    pub deltas: Vec<VoxelChunkDelta>,
}

